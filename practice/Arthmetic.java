//wrp to perform arthmetic operations on 3 nums;
class Arthmetic
{
	public static void main(String[] args)
	{
		int a=10,b=20,c=15;
		System.out.println("Before Arthmetic Operations Value of a = "+a+" b = "+b+" & c = "+c);
		System.out.println("Performing '+' Operation : "+(a+b+c));
		System.out.println("Performing '-' Operation : "+(a-b-c));
		System.out.println("Performing '*' Operation : "+(a*b*c));
		System.out.println("Performing '/' Operation on a/b : "+(a/b));
		System.out.println("Performing '%' Operation on b%c : "+(b%c));
		
	}
}